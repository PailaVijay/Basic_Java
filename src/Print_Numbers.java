
public class Print_Numbers {

	public static void main(String[] args) {
		
		// print 1 to 100 without using numbers
		
		int one=10/10; //1
		String s="..........";
		
		for(int i=one;i<=(s.length()*s.length());i++)
		{
			
			System.out.println(i);
		}
		// using ASCII value
		
		for(int i=one;i<='d';i++)
		{
			System.out.println(i);
		}
		
		
		

	}

}
